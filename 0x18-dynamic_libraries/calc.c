#include <stdio.h>
/**
* add - function name that adds two integers
* @a: first number
* @b: second number
* Return: addition
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - function name that subtracts two integers
* @a: first number
* @b: second number
* Return: subtracted value
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
* mul - function name that multiplies two integers
* @a: first number
* @b: second number
* Return: value multiplied
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div - function name that divides two integers
* @a: first number
* @b: second number
* Return: valued of the divided variables
*/
int div(int a, int b)
{
	return (a / b);
}

/**
* mod - function name that finds modulus of two integers
* @a: first number
* @b: second number
* Return: modulus
*/
int mod(int a, int b)
{
	return (a % b);
}
